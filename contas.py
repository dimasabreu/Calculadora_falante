

def somar(num1, num2):
    soma = num1 + num2
    resultado = (f"A soma dos numeros é {soma}")
    return resultado


def subtrair(num1, num2):
    sub = num1 - num2
    resultado = (f"A subtração dos numeros é {sub}")
    return resultado


def multiplicar(num1, num2):
    mult = num1 * num2
    resultado = (f"A multiplicação dos numeros é {mult}")
    return resultado


def dividir(num1, num2):
    div = num1 / num2
    resultado = (f"A divisão dos numeros é {div}")
    return resultado


def porcentagem(num1, num2):
    por = (num1 / 100) * num2
    resultado = (f"{num1} porcento de {num2} é {por}")
    return resultado
